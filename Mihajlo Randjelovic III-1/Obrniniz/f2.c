void Prebroj(int *a, int *b, int n, int k)
{
	int *pi, *pj;
	for (pi = a; i < a + n; i++)
	{
		*pj = *pi % k;
		(*(b + *pj))++;                                                                                       
	}
	return;

	void main()
	{
		int* a, * b, n, k;
		a = malloc(n * sizeof(int));
		b = malloc(k * sizeof(int));
	}
}